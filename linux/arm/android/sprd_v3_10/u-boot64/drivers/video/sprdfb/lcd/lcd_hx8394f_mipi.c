static int32_t hx8394f_mipi_init(struct panel_spec *self)
{
	int32_t i;
	LCM_Init_Code *init = init_data;
	unsigned int tag;



	mipi_gen_write_t mipi_gen_write = self->info.mipi->ops->mipi_gen_write;


	mipi_set_cmd_mode_t mipi_set_cmd_mode = self->info.mipi->ops->mipi_set_cmd_mode;
	mipi_dcs_write_t mipi_dcs_write = self->info.mipi->ops->mipi_dcs_write;
	mipi_eotp_set_t mipi_eotp_set = self->info.mipi->ops->mipi_eotp_set;

	LCD_PRINT("lcd_hx8394f_init\n");

	mipi_set_cmd_mode();
	mipi_eotp_set(0,0);
#ifdef CONFIG_LCD_COMPATIBLE_BY_READ_ID_VOLTAGE  //GST huafeizhou140324 add-s
	LCD_PRINT("***lcd_vol_type=%d\n", lcd_vol_type);
	if(lcd_vol_type ==LCD_VOL_0MV)
	{
		init=init_data_0MV;
		for(i = 0; i < ARRAY_SIZE(init_data_0MV); i++){
			tag = (init->tag >>24);
			if(tag & LCM_TAG_SEND){
				mipi_gen_write(init->data, (init->tag & LCM_TAG_MASK));
				udelay(20);
			}else if(tag & LCM_TAG_SLEEP){
				mdelay((init->tag & LCM_TAG_MASK));
			}
			init++;
		}
	}
	else
	{
		init=init_data_2000MV;
		for(i = 0; i < ARRAY_SIZE(init_data_2000MV); i++){
			tag = (init->tag >>24);
			if(tag & LCM_TAG_SEND){
				mipi_gen_write(init->data, (init->tag & LCM_TAG_MASK));
				udelay(20);
			}else if(tag & LCM_TAG_SLEEP){
				mdelay((init->tag & LCM_TAG_MASK));
			}
			init++;
		}	
	}
#else
	for(i = 0; i < ARRAY_SIZE(init); i++){
		tag = (init->tag >>24);
		if(tag & LCM_TAG_SEND){
			mipi_gen_write(init->data, (init->tag & LCM_TAG_MASK));
			udelay(20);
		}else if(tag & LCM_TAG_SLEEP){
			mdelay((init->tag & LCM_TAG_MASK));
		}
		init++;
	}
#endif
	mipi_eotp_set(0,0);

	return 0;
}


static uint32_t hx8394f_readid(struct panel_spec *self)
{
	uint32_t j =0;
	uint8_t read_data[4] = {0};
	int32_t read_rtn = 0;
	uint8_t param[2] = {0};
	mipi_set_cmd_mode_t mipi_set_cmd_mode = self->info.mipi->ops->mipi_set_cmd_mode;
	mipi_force_write_t mipi_force_write = self->info.mipi->ops->mipi_force_write;
	mipi_force_read_t mipi_force_read = self->info.mipi->ops->mipi_force_read;
	mipi_eotp_set_t mipi_eotp_set = self->info.mipi->ops->mipi_eotp_set;

	LCD_PRINT("lcd_hx8394f_mipi read id!\n");

	mipi_set_cmd_mode();
	mipi_eotp_set(0,0);

	for(j = 0; j < 4; j++){
		param[0] = 0x03;
		param[1] = 0x00;
		mipi_force_write(0x37, param, 2);
		read_rtn = mipi_force_read(0x04,3, &read_data[0]);
		LCD_PRINT("lcd_hx8394f_mipi read id 0x04 value is 0x%x, 0x%x, 0x%x!\n", read_data[0], read_data[1], read_data[2]);

		//if(0x83 == read_data[0] && 0x94 == read_data[1] && 0x1A == read_data[2]){
		if(0x83 == read_data[0] && 0x94 == read_data[1]){  //zhangbei 160120 add
				LCD_PRINT("hx8394f success!\n");
				return 0x8394;
			}
	}
	mipi_eotp_set(0,0);

	return 0x0;
}

