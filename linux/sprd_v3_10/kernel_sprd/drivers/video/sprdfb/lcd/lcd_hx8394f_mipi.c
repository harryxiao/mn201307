{
	int32_t i = 0;
	uint32_t j =0;
	LCM_Force_Cmd_Code * rd_prepare = rd_prep_code_1;
	uint8_t read_data1[3] = {0};
	uint8_t read_data2[1] = {0};
	uint8_t read_data3[2] = {0};
	uint8_t param[2] = {0};
	int32_t read_rtn = 0;
	unsigned int tag = 0;

	mipi_force_write_t mipi_force_write = self->info.mipi->ops->mipi_force_write;
	mipi_force_read_t mipi_force_read = self->info.mipi->ops->mipi_force_read;
	mipi_eotp_set_t mipi_eotp_set = self->info.mipi->ops->mipi_eotp_set;

	pr_debug("lcd_hx8394f_mipi read power mode!\n");
	mipi_eotp_set(0,0);
	for(j = 0; j < 4; j++){
		rd_prepare = rd_prep_code_1;
		for(i = 0; i < ARRAY_SIZE(rd_prep_code_1); i++){
			tag = (rd_prepare->real_cmd_code.tag >> 24);
			if(tag & LCM_TAG_SEND){
				mipi_force_write(rd_prepare->datatype, rd_prepare->real_cmd_code.data, (rd_prepare->real_cmd_code.tag & LCM_TAG_MASK));
			}else if(tag & LCM_TAG_SLEEP){
				msleep((rd_prepare->real_cmd_code.tag & LCM_TAG_MASK));
			}
			rd_prepare++;
		}
		param[0] = 0x03;
		param[1] = 0x00;
		mipi_force_write(0x37, param, 2);
		read_rtn = mipi_force_read(0x09, 3,(uint8_t *)read_data1);
		//printk("lcd_hx8389b mipi read power mode 0x09 value is 0x%x,0x%x, 0x%x! , read result(%d)\n", read_data1[0],read_data1[1],read_data1[2], read_rtn);
		param[0] = 0x01;
		param[1] = 0x00;
		mipi_force_write(0x37, param, 2);
		read_rtn = mipi_force_read(0xD9, 1,(uint8_t *)read_data2);
		//printk("lcd_hx8389b mipi read power mode 0xD9 value is 0x%x! , read result(%d)\n", read_data2[0],read_rtn);
		param[0] = 0x02;
		param[1] = 0x00;
		mipi_force_write(0x37, param, 2);
		read_rtn = mipi_force_read(0x45, 2,(uint8_t *)read_data3);
		//printk("lcd_hx8389b mipi read power mode 0x45 value is 0x%x,0x%x! , read result(%d)\n", read_data3[0],read_data3[1],read_rtn);
		if((0x80 == read_data1[0])&&(0x73 == read_data1[1])&&(0x04 == read_data1[2])
		//if(0x80 == read_data1[0]){
		   &&(0x80 == read_data2[0])&&(0x05 == read_data3[0])&&(0x34 == read_data3[1])){
			//printk("lcd_hx8389b_mipi read power mode success!\n");
			mipi_eotp_set(0,0);
			return 0x1c;
		}
	}

	//printk("lcd_hx8394f mipi read power mode fail!0x0A value is 0x%x! , read result(%d)\n", read_data[0], read_rtn);
	mipi_eotp_set(0,0);
	return 0x0;
}

static int32_t hx8394f_check_esd(struct panel_spec *self)
{
	uint32_t power_mode;

	mipi_set_lp_mode_t mipi_set_data_lp_mode = self->info.mipi->ops->mipi_set_data_lp_mode;
	mipi_set_hs_mode_t mipi_set_data_hs_mode = self->info.mipi->ops->mipi_set_data_hs_mode;
	mipi_set_lp_mode_t mipi_set_lp_mode = self->info.mipi->ops->mipi_set_lp_mode;
	mipi_set_hs_mode_t mipi_set_hs_mode = self->info.mipi->ops->mipi_set_hs_mode;
	uint16_t work_mode = self->info.mipi->work_mode;

	pr_debug("hx8394f_check_esd!\n");
#ifndef FB_CHECK_ESD_IN_VFP
	if(SPRDFB_MIPI_MODE_CMD==work_mode){
		mipi_set_lp_mode();
	}else{
		mipi_set_data_lp_mode();
	}
#endif
	power_mode = hx8394f_readpowermode(self);
	//power_mode = 0x0;
#ifndef FB_CHECK_ESD_IN_VFP
	if(SPRDFB_MIPI_MODE_CMD==work_mode){
		mipi_set_hs_mode();
	}else{
		mipi_set_data_hs_mode();
	}
#endif
	if(power_mode == 0x1c){
		pr_debug("hx8394f_check_esd OK!\n");
		return 1;
	}else{
		printk("hx8394f_check_esd fail!(0x%x)\n", power_mode);
		return 0;
	}
}

